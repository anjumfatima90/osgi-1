	static {
		int l = data.length;
		for(int i=0;i<l;i++) {
			if(data[i].contains(" ")) {
				LOGGER.debug("" + i +":"+ data[i] + "contains space");

			}
			if(data[i].contains("*")) {
				LOGGER.debug("" + i +":"+ data[i] + "contains *");
			}
		}

		HashSet<String> shortset = new HashSet<String>();
		for(int i=0;i<l/2;i++) {

			String longname = data[i*2];
			String shortname = data[i*2+1];

			if( shortset.contains(shortname) ) {
				LOGGER.debug("shortname "+ shortname + " is duplicated.");
			}
			shortset.add(shortname);

			if ( map.get(longname) != null ) {
				LOGGER.debug(longname +" is duplicated. #line:"+(i+21) );
			}
			map.put(data[i*2],data[i*2+1]);
		}
	}
